create table personen (
    id integer primary key
        generated by default as identity (start with 1000), 
    familienaam varchar(256),
    voornaam varchar(256)   
);

create table contactcodes (
    id char primary key,
    naam varchar(256)
);

create table contactgegevens (
    id integer primary key
        generated by default as identity (start with 1000),
    p_id integer references personen on delete cascade,
    code char(1) references contactcodes,
    adres varchar(256)
);

